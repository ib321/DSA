package com.ib.learn.t01basic.p001codebasic;

public class EvenOdd {
    public static void main(String[] args) {
        int number = 29;

        if (number % 2 == 0) {
            System.out.println(number + " is even.");
        } else {
            System.out.println(number + " is odd.");
        }
    }
}
